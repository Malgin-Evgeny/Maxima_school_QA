public class Harakterstiki extends Main {

    public Harakterstiki(String name, String lastname, int age, String dateofBirth, String work,
                  String adress, float weight, double height, String education, String pasport){
        this.name = name;
        this.lastname = lastname;
        this.age = age;
        this.dateofBirth = dateofBirth;
        this.work = work;
        this.adress = adress;
        this.weight = weight;
        this.height = height;
        this.education = education;
        this.pasport = pasport;
    }



    protected String name;
    protected String lastname;

    protected int age;
    protected String dateofBirth;
    protected String work;
    protected String adress;
    protected float weight;
    protected double height;
    protected String education;
    protected String pasport;

}

